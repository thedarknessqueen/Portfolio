//Ouverture et recupération des données présentes dans le fichier texte (sous forme de string)
string Fichier::lecture() {
    string full;
    string contenu;
    string const nomFichier("C:/Users/thais/Documents Local/CPI A2/Projet 2/Livrable/Projet-2/Fichier.txt");//(Entre guillement le chemin du fichier, monFlux est le nom du contenue du fichier)
    ifstream monFlux(nomFichier.c_str());
    while (getline(monFlux, contenu)) {
        if(monFlux)  //Teste si l'ouverture a fonctionné
        {
            full +=  contenu + "\n";
        }
        else
            cout << "Impossible d'ouvrir le fichier !" << endl;
    }
    monFlux.close();
    return full;
}

//transformation de la string du fichier en une serie de vecteurs (matrice en 2D)
vector<string> Fichier::vecteur(const string& txt) {
    vector<string> liste;
    stringstream ss(txt);
    string ligne;
    while (getline(ss,ligne)) {
        liste.push_back(ligne);
    }
    return liste;
}

void Fichier::ecriture(const string& nomFichier, const vector<string>& grille) {
    ofstream fichier(nomFichier);  // Ouvre le fichier en mode écriture
    if (!fichier.is_open()) {
        cerr << "Erreur : Impossible d'ouvrir le fichier " << nomFichier << endl;
        return;
    }

    // Écrire la grille dans le fichier
    for (const auto& ligne : grille) {
        fichier << ligne << '\n';
    }

    fichier.close();
    cout << "État écrit dans : " << nomFichier << endl;
}

